"""CLI commands for the Loop engineering layer.

    tvastar loop init <Pattern> [--name NAME] [--out PATH]
    tvastar loop run  <ref>                   e.g. .tvastar/loops/ci.py:loop
    tvastar loop status <ref>
    tvastar loop audit <ref>

Werner principle: fail fast at configuration time, not at 2am.
Every command validates inputs before touching any runtime state.
"""

