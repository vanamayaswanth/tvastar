def _print_run(run: "LoopRun") -> None:  # type: ignore[name-defined]
    import time as _time

    dur = run.duration
    dur_s = f"{dur:.1f}s" if dur is not None else "?"
    ts_raw = run.ended_at or run.started_at
    ts = _time.strftime("%Y-%m-%d %H:%M:%S UTC", _time.gmtime(ts_raw))
    print(f"\nRun {run.run_id[:8]}  [{run.state.value.upper()}]  {dur_s}  {ts}")

    if run.result_text:
        snippet = run.result_text[:400].replace("\n", " ")
        ellipsis = "…" if len(run.result_text) > 400 else ""
        print(f"  Result: {snippet}{ellipsis}")

    if run.error:
        print(f"  Error:  {run.error}")

    if run.findings:
        print(f"  Findings ({len(run.findings)}):")
        for finding in run.findings:
            sev = getattr(finding.severity, "value", finding.severity)
            print(f"    [{sev.upper()}] {finding.message}")


def _print_status(loop: "Loop") -> None:  # type: ignore[name-defined]
    import time as _time

    from . import LoopState

    _hr = "─" * 52
    print(f"\n{_hr}")
    print(f"  Loop:     {loop.name}")
    print(f"  State:    {loop.state.value.upper()}")
    print(f"  Schedule: {loop.config.schedule}")

    last = loop.last_run()
    if last:
        ts_raw = last.ended_at or last.started_at
        ts = _time.strftime("%Y-%m-%d %H:%M UTC", _time.gmtime(ts_raw))
        dur_s = f"{last.duration:.1f}s" if last.duration is not None else "?"
        print(f"  Last run: [{last.state.value.upper()}]  {ts}  ({dur_s})")
        if last.result_text:
            snippet = last.result_text[:120].replace("\n", " ")
            ellipsis = "…" if len(last.result_text) > 120 else ""
            print(f"            {snippet}{ellipsis}")
    else:
        print("  Last run: (never run)")

    if loop.config.schedule != "@manual" and loop.state not in (
        LoopState.SUSPENDED,
        LoopState.RUNNING,
        LoopState.VERIFYING,
        LoopState.TRIGGERED,
    ):
        try:
            from datetime import datetime, timezone

            from .schedule import next_run_time

            nxt = next_run_time(loop.config.schedule, datetime.now(tz=timezone.utc))
            print(f"  Next run: {nxt.strftime('%Y-%m-%d %H:%M UTC')}")
        except ValueError:
            pass

    if loop.state == LoopState.SUSPENDED:
        print(
            "\n  SUSPENDED — circuit breaker tripped.\n"
            "  To resume: call loop.reset() in a Python script."
        )
    print(f"{_hr}\n")


def _print_audit(loop: "Loop", report: "ReadinessLevel") -> None:  # type: ignore[name-defined]
    _hr = "━" * 52
    badge = f"L{report.level} · {report.name}"

    print(f"\n{_hr}")
    print(f"  Loop Readiness Audit — {loop.name}")
    print(_hr)
    print(f"  Level:  {badge}")
    print(f"  {report.description}")

    if report.passes:
        print()
        print("  Passes:")
        for p in report.passes:
            print(f"    ✓  {p}")

    if report.gaps:
        print()
        print("  Gaps (blocking — fix to advance level):")
        for g in report.gaps:
            print(f"    ✗  {g}")

    if report.warnings:
        print()
        print("  Warnings (non-blocking):")
        for w in report.warnings:
            print(f"    ⚠  {w}")

    production = "YES — this loop can run unattended." if report.is_production_ready else "NO"
    print()
    print(f"  Production-ready: {production}")
    print(f"{_hr}\n")


