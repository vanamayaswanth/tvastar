def cmd_init(pattern: str, name: str | None, out: str | None) -> int:
    """Scaffold a loop config file from a named pattern.

    Failure modes handled:
    - Unknown pattern → list available patterns and exit 1
    - Destination file already exists → refuse to overwrite, exit 1
    - Parent directories missing → create them automatically
    """
    if pattern not in _PATTERNS:
        print(
            f"Unknown pattern: {pattern!r}\nAvailable patterns: {', '.join(_PATTERNS)}",
            file=sys.stderr,
        )
        return 1

    loop_name = name or _PATTERNS[pattern]
    safe_name = loop_name.replace("-", "_")

    dest = Path(out) if out else Path(".tvastar") / "loops" / f"{safe_name}.py"

    if dest.exists():
        print(
            f"File already exists: {dest}\n"
            "Remove it first or specify --out to write to a different path.",
            file=sys.stderr,
        )
        return 1

    dest.parent.mkdir(parents=True, exist_ok=True)
    dest.write_text(_scaffold(pattern, loop_name), encoding="utf-8")

    print(f"Scaffolded {pattern} → {dest}")
    print(f"  1. Edit {dest} — set your model and review the schedule.")
    print(f"  2. Run:  tvastar loop audit {dest}:loop")
    print(f"  3. Run:  tvastar loop run   {dest}:loop")
    return 0


def cmd_run(ref: str) -> int:
    """Trigger a loop once (blocking) and print the result.

    Exit 0 on PASS, exit 1 on any failure state.

    Failure modes handled:
    - Loop file not found → clear error, exit 1
    - Loop SUSPENDED → block with reset instructions, exit 1
    - Run fails (FAIL/HANDOFF/etc.) → print error details, exit 1
    """
    from ..errors import TvastarError

    try:
        loop = _load_loop(ref)
    except TvastarError as exc:
        print(f"Error: {exc}", file=sys.stderr)
        return 1

    from . import LoopState

    if loop.state == LoopState.SUSPENDED:
        print(
            f"Loop '{loop.name}' is SUSPENDED (circuit breaker tripped after "
            f"{loop.config.circuit_breaker_limit} consecutive failures).\n"
            "To resume: call loop.reset() in a Python script.",
            file=sys.stderr,
        )
        return 1

    async def _go() -> int:
        run = await loop.trigger()
        _print_run(run)
        return 0 if run.ok else 1

    return asyncio.run(_go())


def cmd_status(ref: str) -> int:
    """Show current state, last run, and next scheduled time for a loop."""
    from ..errors import TvastarError

    try:
        loop = _load_loop(ref)
    except TvastarError as exc:
        print(f"Error: {exc}", file=sys.stderr)
        return 1

    _print_status(loop)
    return 0


def cmd_audit(ref: str) -> int:
    """Score a loop's readiness (L0→L3) and print the report.

    Exit 0 only at L3 AUTONOMOUS (production-ready).
    Suitable for use in CI/pre-deploy gates:
        tvastar loop audit .tvastar/loops/ci.py:loop || exit 1
    """
    from ..errors import TvastarError

    try:
        loop = _load_loop(ref)
    except TvastarError as exc:
        print(f"Error: {exc}", file=sys.stderr)
        return 1

    from .audit import audit_loop

    report = audit_loop(loop)
    _print_audit(loop, report)
    return 0 if report.is_production_ready else 1


