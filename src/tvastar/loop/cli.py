def _scaffold(pattern: str, name: str) -> str:
    if pattern == "MakerChecker":
        return _MAKER_CHECKER_TEMPLATE.format(name=name)
    return _STANDARD_TEMPLATE.format(pattern=pattern, name=name)


# ---------------------------------------------------------------------------
# Loop loader
# ---------------------------------------------------------------------------


def _load_loop(ref: str) -> "Loop":  # type: ignore[name-defined]
    """Resolve ``file.py:attr`` or ``module:attr`` to a Loop instance."""
    import importlib
    import importlib.util

    from ..errors import TvastarError
    from . import Loop

    target, _, attr = ref.partition(":")
    attr = attr or "loop"

    if target.endswith(".py") or "/" in target or "\\" in target:
        p = Path(target).resolve()
        if not p.exists():
            raise TvastarError(f"Loop file not found: {target}")
        file_spec = importlib.util.spec_from_file_location(p.stem, p)
        if file_spec is None or file_spec.loader is None:
            raise TvastarError(f"Cannot import {target}")
        module = importlib.util.module_from_spec(file_spec)
        sys.modules[p.stem] = module
        file_spec.loader.exec_module(module)  # type: ignore[union-attr]
    else:
        try:
            module = importlib.import_module(target)
        except ImportError as exc:
            raise TvastarError(f"Cannot import {target!r}: {exc}") from exc

    if not hasattr(module, attr):
        available = [k for k, v in vars(module).items() if isinstance(v, Loop)]
        hint = f" Available Loop objects: {available}" if available else ""
        raise TvastarError(f"'{target}' has no attribute '{attr}'.{hint}")

    obj = getattr(module, attr)
    if not isinstance(obj, Loop):
        raise TvastarError(f"'{ref}' resolved to {type(obj).__name__}, expected a Loop instance.")
    return obj


